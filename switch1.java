import java.util.*;
public class switch1 {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        int number = sc.nextInt();
        switch(number){
            case 1 : System.out.println("Male");
                     break;
            case 3 : System.out.println("Female");
                     break;
            case 6 : System.out.println("guy");
        }

    }
}
